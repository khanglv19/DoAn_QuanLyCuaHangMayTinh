    public void hienThi(){
        ArrayList<SessionPOJO> dSS = SessionDAO.xemSession();
        dtm.setRowCount(0);
        for(SessionPOJO ss: dSS){
            Vector<Object> vec = new Vector<Object>();
            vec.add(ss.getSid());
            vec.add(ss.getSerial());
            vec.add(ss.getUsername());
            vec.add(ss.getSchemaname());
            vec.add(ss.getProgram());
            vec.add(ss.getTerminal());
            dtm.addRow(vec);
        }
        tblSession.setModel(dtm);
    }

