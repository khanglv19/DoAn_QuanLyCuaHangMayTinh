    private void tblSessionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSessionMouseClicked
        // TODO add your handling code here:
        int row = tblSession.getSelectedRow();
        txtSid.setText((String) tblSession.getValueAt(row, 0));
        txtSerial.setText((String) tblSession.getValueAt(row, 1));
    }//GEN-LAST:event_tblSessionMouseClicked

    private void btnKillSessionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKillSessionActionPerformed
        // TODO add your handling code here:
        if(txtSid.getText().isEmpty() && txtSerial.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Chọn SID và SERIAL# cần xóa!", "Thông báo!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String sId = txtSid.getText();
        String serial = txtSerial.getText();
        SessionPOJO ss = new SessionPOJO();
        ss.setSid(sId);
        ss.setSerial(serial);
        SessionDAO.killSession(sId,serial);
//        try {
//            if(SessionDAO.killSession(sId,serial) == false)
//            {
//                JOptionPane.showMessageDialog(null, "Kill Session thất bại!");
//            }
//        } catch (Exception e) {
//            JOptionPane.showMessageDialog(null, "Kill Session thành công!");
//        }
        hienThi();
    }//GEN-LAST:event_btnKillSessionActionPerformed

