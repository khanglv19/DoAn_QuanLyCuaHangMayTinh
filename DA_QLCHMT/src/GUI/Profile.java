    public void hienThi(){
        ArrayList<ProfilePOJO> dsPrf = ProfileDAO.xemProfile();
        dtm.setRowCount(0);
        for(ProfilePOJO prf: dsPrf){
            Vector<Object> vec = new Vector<Object>();
            vec.add(prf.getProfile());
            vec.add(prf.getResource_name());
            vec.add(prf.getResource_type());
            vec.add(prf.getLimit());
            dtm.addRow(vec);
        }
        tblProfile.setModel(dtm);
    }
    

