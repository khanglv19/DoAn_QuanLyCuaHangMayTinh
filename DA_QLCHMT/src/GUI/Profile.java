    private void btnTaoProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoProfileActionPerformed
            String tenPrfl = txtTenPrfl.getText();
            String ssnsPerUsr = txtSsnsPerUsr.getText();
            String cpuPerSsn = txtCpuPerSsn.getText();
            String flLgnAtp = txtFlLgnAtmp.getText();
            ProfilePOJO prfl = new ProfilePOJO();
            prfl.setProfile(tenPrfl);
            prfl.setSsnsPerUsr(ssnsPerUsr);
            prfl.setCpuPerSssn(cpuPerSsn);
            ProfileDAO.taoProfile(tenPrfl, ssnsPerUsr, cpuPerSsn, flLgnAtp);
            hienThi();
    }//GEN-LAST:event_btnTaoProfileActionPerformed

    private void btnXoaPrfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaPrfActionPerformed
        if(txtTenPrfl.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nhập tên profile cần xóa!", "Thông báo!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String tenPrf = txtTenPrfl.getText();
        ProfilePOJO sp = new ProfilePOJO();
        sp.setTenProfile(tenPrf);
        ProfileDAO.xoaProfile(tenPrf);
        hienThi();
    }//GEN-LAST:event_btnXoaPrfActionPerformed

    private void tblProfileMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProfileMouseClicked
        int row = tblProfile.getSelectedRow();
        txtTenPrfl.setText((String) tblProfile.getValueAt(row, 0));
    }//GEN-LAST:event_tblProfileMouseClicked

