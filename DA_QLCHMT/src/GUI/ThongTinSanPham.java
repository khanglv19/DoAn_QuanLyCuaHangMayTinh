    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(txtMaHang.getText().isEmpty() && txtTenHang.getText().isEmpty() && txtNSX.getText().isEmpty() && txtGiaBan.getText().isEmpty() && txtSoLuongTon.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Không được bỏ trống!", "Thông báo!", JOptionPane.INFORMATION_MESSAGE);
        }
        String maSP = txtMaHang.getText();
        String tenSP = txtTenHang.getText();
        String nsx = txtNSX.getText();
        float gia = Float.parseFloat(txtGiaBan.getText());
        int soLuong = Integer.parseInt(txtSoLuongTon.getText());
        SanPhamPOJO sp = new SanPhamPOJO();
        sp.setMaHang(maSP);
        sp.setTenHang(tenSP);
        sp.setNsx(nsx);
        sp.setGiaBan(gia);
        sp.setSoLuongTon(soLuong);
        SanPhamDAO.themSanPham(sp);
        hienThi();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        String maSP = txtMaHang.getText();
        String tenSP = txtTenHang.getText();
        String nsx = txtNSX.getText();
        float gia = Float.parseFloat(txtGiaBan.getText());
        int soLuong = Integer.parseInt(txtSoLuongTon.getText());
        SanPhamPOJO sp = new SanPhamPOJO(maSP, tenSP, nsx, gia, soLuong);
        SanPhamDAO.capNhatSanPham(sp);
        hienThi();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if(txtMaHang.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nhập mã cần xóa!", "Thông báo!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String maSP = txtMaHang.getText();
        SanPhamPOJO sp = new SanPhamPOJO();
        sp.setMaHang(maSP);
        SanPhamDAO.xoaSanPham(maSP);
        hienThi();
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:
        int row = tblSanPham.getSelectedRow();
        txtMaHang.setText((String) tblSanPham.getValueAt(row, 0));
        txtTenHang.setText((String) tblSanPham.getValueAt(row, 1));
        txtNSX.setText((String) tblSanPham.getValueAt(row, 2));
        txtGiaBan.setText((float) tblSanPham.getValueAt(row, 3) + "");
        txtSoLuongTon.setText((int) tblSanPham.getValueAt(row, 4) + "");
    }//GEN-LAST:event_tblSanPhamMouseClicked

