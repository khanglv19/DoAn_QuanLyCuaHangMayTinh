    public void hienThi(){
        ArrayList<SanPhamPOJO> dsSP = SanPhamDAO.layDanhSachSanPham();
        dtm.setRowCount(0);
        for(SanPhamPOJO sp: dsSP){
            Vector<Object> vec = new Vector<Object>();
            vec.add(sp.getMaHang());
            vec.add(sp.getTenHang());
            vec.add(sp.getNsx());
            vec.add(sp.getGiaBan());
            vec.add(sp.getSoLuongTon());
            dtm.addRow(vec);
        }
        tblSanPham.setModel(dtm);
    }

